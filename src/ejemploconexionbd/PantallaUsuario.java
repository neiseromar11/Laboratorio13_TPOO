   private void filtrarUsuarios(){
   List<Usuario> usuarios = new ArrayList<>();
   for (Usuario user: usuariosLista){
      if (user.getUsuarioText().toLowerCase().contains(txtBuscar.getText().toLowerCase())) {
            usuarios.add(user);
        }
   }
   usuariosLista=usuarios;
   actualizarModelo();
   }
    
    
    private void eliminarUsuario(Usuario user) {
        try{
            DataAccessUsuario dao = new DataAccessUsuario(st,user);
            dao.eliminarUsuario();
        }catch(Exception s){
            System.out.println(s.getMessage());
        }
    }
