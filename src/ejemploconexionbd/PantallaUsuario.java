    private void contarseDB() {
        try{
            conexion=DatabaseConnection.getConnection();
            st=conexion.createStatement();
        }catch(Exception s ){
            System.out.println(s.getMessage());
        }
    }

    private void listarUsuarios(){
        try{
        DataAccessUsuario dao = new DataAccessUsuario(st);
        usuariosLista= dao.getListaUsuarios();
        actualizarModelo();
        }
        catch(Exception s){
            System.out.println(s.getMessage());
        }
    }

    private void actualizarModelo() {
        modeloLista.clear();
        for(Usuario user: usuariosLista){
            modeloLista.addElement(user.getUsuarioText());
            
        }
    }
    
